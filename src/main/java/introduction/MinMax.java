package introduction;

public class MinMax {

    public static void main(String[] args) {
        long minlong = Long.MIN_VALUE;
        long maxlong = Long.MAX_VALUE;
        System.out.println("Minimalna wartosc long to " + minlong);
        int minint = Integer.MIN_VALUE;
        int maxint = Integer.MAX_VALUE;
        System.out.println("Minimalna wartosc int " + minint);
        System.out.println("Maksymalna wartosc int " + maxint);


        double mindouble = Double.MIN_VALUE;
        double maxdouble = Double.MAX_VALUE;
        System.out.println("Minimalna wartosc Double " + mindouble);
        System.out.println("Maksymalna wartosc Double " + maxdouble);

        float minfloat = Float.MIN_VALUE;
        float maxfloat = Float.MAX_VALUE;
        System.out.println("Minimalna wartosc Float " + minfloat);
        System.out.println("Maksymalna wartosc Float " + maxfloat);
        //W tym zadaniu dowiedziałem sie jak wyświetlać  wartosci minimalne i maksymalne w typach .
    }
}
